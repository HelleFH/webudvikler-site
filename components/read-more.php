        <h3> Webudvikler </h3>
    </summary>
    <p>
        Som webudvikler vil du have et bredere ansvarsområde, da du både vil arbejde med
        frontend og backend. Du vil være ansvarlig for at udvikle og implementere avancerede
        løsninger og funktioner, der er tilpasset specifikke kundebehov og teknologiske krav. Det
        betyder, at du skal have en dybdegående viden om både frontend og backend teknologier, og kunne
        arbejde med forskellige programmeringssprog og databaser. </p>
</details>
